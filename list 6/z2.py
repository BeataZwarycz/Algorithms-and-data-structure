class BinHeap:
    def __init__(self):  # create BinHeap
        self.heapList = [0]  # as a list
        self.currentSize = 0

    def percUp(self, i):
        while i // 2 > 0:
            if self.heapList[i] < self.heapList[i // 2]:
                tmp = self.heapList[i // 2]
                self.heapList[i // 2] = self.heapList[i]
                self.heapList[i] = tmp
            i = i // 2

    def insert(self, k):
        self.heapList.append(k)
        self.currentSize = self.currentSize + 1
        self.percUp(self.currentSize)

    def findMin(self):
        return self.heapList[1]

    def percDown(self, i):
        while (i * 2) <= self.currentSize:
            mc = self.minChild(i)
            if self.heapList[i] > self.heapList[mc]:
                tmp = self.heapList[i]
                self.heapList[i] = self.heapList[mc]
                self.heapList[mc] = tmp
            i = mc

    def minChild(self, i):
        if i * 2 + 1 > self.currentSize:
            return i * 2
        else:
            if self.heapList[i * 2] < self.heapList[i * 2 + 1]:
                return i * 2
            else:
                return i * 2 + 1

    def delMin(self):
        retval = self.heapList[1]
        self.heapList[1] = self.heapList[self.currentSize]
        self.currentSize = self.currentSize - 1
        self.heapList.pop()
        self.percDown(1)
        return retval

    def buildHeap(self, alist):
        i = len(alist) // 2
        self.currentSize = len(alist)
        self.heapList = [0] + alist[:]
        while (i > 0):
            self.percDown(i)
            i = i - 1

    def size(self):
        return self.currentSize

    def isEmpty(self):
        return self.currentSize == 0

    def __str__(self):
        txt = "{}".format(self.heapList[1:])
        return txt


def heap_sort(alist):
    """Function to sort element in heap."""
    heap = BinHeap()
    heap.buildHeap(alist)  # build heap from list
    sorted_heap = []
    for i in range(len(alist)):  # for each element of given list
        sorted_heap.append(heap.delMin())  # add lowest item from heap to newlist

    return sorted_heap
